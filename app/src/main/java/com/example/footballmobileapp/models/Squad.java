package com.example.footballmobileapp.models;

public class Squad {
    int id;
    String name;
    String position;
    String dateOfBirth;
    String nationality;

    public Squad() {
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPosition() {
        return position;
    }

    public String getDateOfBirth() {
        return dateOfBirth;
    }

    public String getNationality() {
        return nationality;
    }
}
